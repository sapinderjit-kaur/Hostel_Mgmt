     public Boolean check()
    {
        if (txtname.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Fill  name");
           return false;
        }
        if (txtadmissiondate.getText().equals(""))
        {
           JOptionPane.showMessageDialog(this,"Fill admission date");
           return false;
        }
        if (txtfathername.getText().equals("")) 
        {
            JOptionPane.showMessageDialog(this,"Fill The Fathers Name");
            return false;
        }
        
        if (txtaddress.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Fill  Address");
            return false;
           }
        if (txtcontactno.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Fill  Contact No");
            
        }
        String contact_no=txtcontactno.getText();
        if (!contact_no.matches("^(?:(?:\\+|0{0,2})91(\\s*[\\-]\\s*)?|[0]?)?[789]\\d{9}$")) {
            JOptionPane.showMessageDialog(this, "Fill Correct Contact No");
            return false;
        }
       
        return true;
    }

