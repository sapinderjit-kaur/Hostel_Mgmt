    private void tbstudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbstudentMouseClicked
btnupdate.setEnabled(true);
        btndalete.setEnabled(true);
        btnsave.setEnabled(false);
        try {
            int row = tbstudent.getSelectedRow();
            String table_click = tbstudent.getModel().getValueAt(row, 0).toString();
            String str = "select * from registrations where regid='" + table_click + "'";
            pst = con.prepareStatement(str);
            rs = pst.executeQuery();
            if (rs.next()) {
                String id = rs.getString("regid");
                txtid.setText(id);
                String first_name = rs.getString("name");
                txtname.setText(first_name);
                String admdate = rs.getString("admissiondate");
                txtadmissiondate.setText(admdate);
                
               
                String category= rs.getString("category");
                txtcategory.setText(category);
                String fathername = rs.getString("fathername");
                txtfathername.setText(fathername);
                String dob = rs.getString("dob");
                txtdob.setText(dob);
                 String address = rs.getString("address");
                txtaddress.setText(address);
                String contact = rs.getString("contact");
                txtcontactno.setText(contact);
                String email=rs.getString("email");
                txtemail.setText(email);
                String nation=rs.getString("nationality");
                txtnationality.setText(nation);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_tbstudentMouseClicked

